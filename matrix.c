ws2811_led_t dotcolors[] =
{
    0x00200000,  // red
    0x00201000,  // orange
    0x00202000,  // yellow
    0x00002000,  // green
    0x00002020,  // lightblue
    0x00000020,  // blue
    0x00100010,  // purple
    0x00200010,  // pink
};


void initialize_matrix(void){
    ws2811_init(&ledstring);
}


void deinitalize_matrix(void){
    ws2811_fini(&ledstring);
}

void matrix_clear(void){
    for (int i=0; i<LED_COUNT; i++){
        ledstring.channel[0].leds[i] = 0;
    }
    ws2811_render(&ledstring);
}

void draw_matrix(unsigned char mat[][32]){
    int position;
    for (int y=0; y<32; y++){
        for (int x=0; x<32; x++){
            if (mat[y][x] == 1){
                if (y < 16){
                    if(x%2 == 0){
                        position = 16*x + y;
                    } else {
                        position = (16*x) + (15 - y);
                    }
                } else {
                    if(x%2 == 0){
                        position = (512 + 16*x) + (y - 16);
                    } else {
                        position = (512 + 16*x) + (31 - y);
                    }
                }
                ledstring.channel[0].leds[position] = dotcolors[0];
            }
                
        }
    }

    ws2811_render(&ledstring);
}


void draw_A(void){
    draw_matrix(PRINT_A);
    ws2811_render(&ledstring);
}

void animate_A(void){
    unsigned char i;
    for (i=0; i<161; i++){
        ledstring.channel[0].leds[ANIMATE_A[i]] = dotcolors[0];
        if (i%2 != 0){
            ws2811_render(&ledstring);
            usleep(10000);
        }
    }
    ws2811_render(&ledstring);
}

